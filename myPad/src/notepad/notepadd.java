    private void customInit() {
        this.currentSelectedTextArea = (AFCTextArea) this.tabbedPane.getSelectedComponent();
        this.currentSelectedTextArea.textArea.setDropTarget(new DropTarget() {
            @Override
            public void drop(DropTargetDropEvent dtde) {
                try {
                    dtde.acceptDrop(DnDConstants.ACTION_COPY_OR_MOVE);
                    List<File> list = (List<File>) dtde.getTransferable().getTransferData(DataFlavor.javaFileListFlavor);
                    list.forEach((file) -> {
                        createNewTab(file);
                    });

                } catch (UnsupportedFlavorException | IOException e) {
                }
            }
        });

        this.tabbedPane.addChangeListener(((e) -> {
            this.currentSelectedTextArea = (AFCTextArea) this.tabbedPane.getSelectedComponent();

            this.currentSelectedTextArea.textArea.setDropTarget(new DropTarget() {
                @Override
                public void drop(DropTargetDropEvent dtde) {
                    try {
                        dtde.acceptDrop(DnDConstants.ACTION_COPY_OR_MOVE);
                        List<File> list = (List<File>) dtde.getTransferable().getTransferData(DataFlavor.javaFileListFlavor);
                        list.forEach((file) -> {
                            createNewTab(file);
                        });

                    } catch (UnsupportedFlavorException | IOException e) {
                    }
                }
            });
        }));

    }

    private void createNewTab(File file) {

        AFCTextArea textArea = new AFCTextArea();
        LoadWorker twerk = new LoadWorker(textArea.textArea, file);
        twerk.execute();
        textArea.path = file.toPath();
        this.tabbedPane.addTab(textArea.getFileName(), textArea);
        this.tabbedPane.setSelectedIndex(this.tabbedPane.getComponentCount() - 1);

    }

