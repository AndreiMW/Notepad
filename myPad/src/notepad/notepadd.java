    private void saveFile(File file) {
        file = fc.getSelectedFile();
        String text2save = this.currentSelectedTextArea.textArea.getText();

        try {
            BufferedWriter bw = new BufferedWriter(new FileWriter(file));
            bw.write(text2save);
            bw.close();
        } catch (IOException e) {
        }
    }

